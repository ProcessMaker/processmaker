<template>
  <div id="start-events btn-group mx-2">
    <button
      v-if="havelessOneStartEvent"
      class="btn btn-success start-button"
      type="button"
      :disabled="processEvents.length === 0"
      @click="goToNewRequest(startEvent)"
    >
      <i class="fa fa-play-circle" style="font-size: 16px;" />
      <span class="pl-2"> {{ $t('Start this process') }} </span>
    </button>
    <button
      v-else
      class="btn btn-success btn-lg dropdown-toggle start-button justify-content-between"
      type="button"
      data-toggle="dropdown"
      aria-haspopup="true"
      aria-expanded="false"
      @click="getStartEvents()"
    >
      <span>
        <i class="fa fa-play-circle" style="font-size: 16px;"></i>
        <span class="pl-2"> {{ $t('Start this process') }} </span>
      </span>
      <i class="fas fa-caret-down" style="font-size: 16px;"></i>
    </button>
    <div class="dropdown-menu scrollable-menu p-3 pb-0 mt-2">
      <p
        class="font-weight-bold px-1 text-uppercase"
        style="font-size: 14px"
      >
        {{ $t('Starting events') }}
      </p>
      <div
        v-for="event in processEvents"
        :key="event.id"
        class="mt-2"
      >
        <p
          class="text-capitalize mb-1"
          style="font-weight: 600"
        >
          {{ event.name }}
        </p>
        <button
          v-if="event.webEntry"
          type="button"
          class="btn btn-outline-primary border-0 p-1 text-capitalize"
          @click="copyLink(event)"
        >
          <i class="fas fa-link p-1" />
          {{ $t('Copy Link') }}
        </button>
        <button
          v-else
          type="button"
          class="btn btn-outline-success border-0 p-1 text-capitalize"
          @click="goToNewRequest(event.id)"
        >
          <i class="fas fa-play-circle p-1" />
          {{ $t('Start') }}
        </button>
        <hr class="mt-2 mb-0">
      </div>
    </div>
  </div>
</template>

<script>
export default {
  props: ["process"],
  data() {
    return {
      processEvents: [],
      havelessOneStartEvent: false,
      startEvent: "",
    };
  },
  mounted() {
    this.getStartEvents();
  },
  methods: {
    /**
     * get start events for dropdown Menu
     */
    getStartEvents() {
      this.processEvents = [];
      ProcessMaker.apiClient
        .get(`process_bookmarks/processes/${this.process.id}/start_events`)
        .then((response) => {
          this.processEvents = response.data.data;
          if (this.processEvents.length <= 1) {
            const event = this.processEvents[0] ?? {};
            if (!("webEntry" in event)) {
              this.havelessOneStartEvent = true;
              this.startEvent = event.id ?? 0;
            }
          }
        })
        .catch(err => {
          this.disableButton();
          ProcessMaker.alert(err, "danger");
        });
    },
    /** 
     * Disable Start Button
     */
    disableButton() {
      this.havelessOneStartEvent = true;
      this.processEvents = [];
      this.startEvent = 0;
    },
    /**
     * Start new request
     */
    goToNewRequest(event) {
      ProcessMaker.apiClient
        .post(`/process_events/${this.process.id}?event=${event}`)
        .then((response) => {
          this.spin = 0;
          const instance = response.data;
          this.$cookies.set("fromTriggerStartEvent", true, "1min");
          window.location = `/requests/${instance.id}?fromTriggerStartEvent=`;
        }).catch((err) => {
          const { data } = err.response;
          if (data.message) {
            ProcessMaker.alert(data.message, "danger");
          }
        });
    },
    /**
     * Copy WebEntry Link
     */
    copyLink(event) {
      const link = event.webEntry.webentryRouteConfig.entryUrl;
      navigator.clipboard.writeText(link);
      if (event.assignment && ["user_group", "user"].includes(event.assignment)) {
        const msg = this.$t("Please use this link when you are not logged into ProcessMaker");
        ProcessMaker.alert(msg, "success", 5, false, false, "", "Anonymous Web Link Copied");
      } else {
        this.$t("Link copied", "success");
      }
    },
  },
};
</script>

